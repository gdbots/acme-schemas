<?php
declare(strict_types=1);

// @link http://acme-schemas.gdbots.io/json-schema/acme/iam/node/android-app/1-0-0.json#
namespace Acme\Schemas\Iam\Node;

use Gdbots\Pbj\AbstractMessage;
use Gdbots\Pbj\Enum\Format;
use Gdbots\Pbj\FieldBuilder as Fb;
use Gdbots\Pbj\Schema;
use Gdbots\Pbj\Type as T;
use Gdbots\Pbj\WellKnown\UuidIdentifier;
use Gdbots\Schemas\Iam\Mixin\AndroidApp\AndroidAppV1Trait as GdbotsIamAndroidAppV1Trait;
use Gdbots\Schemas\Ncr\Enum\NodeStatus;
use Gdbots\Schemas\Ncr\Mixin\Node\NodeV1Trait as GdbotsNcrNodeV1Trait;

final class AndroidAppV1 extends AbstractMessage
{
    const SCHEMA_ID = 'pbj:acme:iam:node:android-app:1-0-0';
    const SCHEMA_CURIE = 'acme:iam:node:android-app';
    const SCHEMA_CURIE_MAJOR = 'acme:iam:node:android-app:v1';

    const MIXINS = [
      'gdbots:ncr:mixin:node:v1',
      'gdbots:ncr:mixin:node',
      'gdbots:iam:mixin:app:v1',
      'gdbots:iam:mixin:app',
      'gdbots:iam:mixin:android-app:v1',
      'gdbots:iam:mixin:android-app',
    ];

    const _ID_FIELD = '_id';
    const STATUS_FIELD = 'status';
    const ETAG_FIELD = 'etag';
    const CREATED_AT_FIELD = 'created_at';
    const CREATOR_REF_FIELD = 'creator_ref';
    const UPDATED_AT_FIELD = 'updated_at';
    const UPDATER_REF_FIELD = 'updater_ref';
    const LAST_EVENT_REF_FIELD = 'last_event_ref';
    const TITLE_FIELD = 'title';
    const ROLES_FIELD = 'roles';
    const AZURE_NOTIFICATION_HUB_CONNECTION_FIELD = 'azure_notification_hub_connection';
    const AZURE_NOTIFICATION_HUB_NAME_FIELD = 'azure_notification_hub_name';
    const FCM_API_KEY_FIELD = 'fcm_api_key';
    const FCM_PROJECT_ID_FIELD = 'fcm_project_id';
    const FCM_SENDER_ID_FIELD = 'fcm_sender_id';
    const FCM_WEB_API_KEY_FIELD = 'fcm_web_api_key';

    const FIELDS = [
      self::_ID_FIELD,
      self::STATUS_FIELD,
      self::ETAG_FIELD,
      self::CREATED_AT_FIELD,
      self::CREATOR_REF_FIELD,
      self::UPDATED_AT_FIELD,
      self::UPDATER_REF_FIELD,
      self::LAST_EVENT_REF_FIELD,
      self::TITLE_FIELD,
      self::ROLES_FIELD,
      self::AZURE_NOTIFICATION_HUB_CONNECTION_FIELD,
      self::AZURE_NOTIFICATION_HUB_NAME_FIELD,
      self::FCM_API_KEY_FIELD,
      self::FCM_PROJECT_ID_FIELD,
      self::FCM_SENDER_ID_FIELD,
      self::FCM_WEB_API_KEY_FIELD,
    ];

    use GdbotsNcrNodeV1Trait;

    use GdbotsIamAndroidAppV1Trait;

    protected static function defineSchema(): Schema
    {
        return new Schema(self::SCHEMA_ID, __CLASS__,
            [
                /*
                 * The "_id" value:
                 * - MUST NOT change for the life of the node.
                 * - SHOULD be globally unique
                 * - SHOULD be generated by the app (ideally in default value closure... e.g. UuidIdentifier::generate())
                 */
                Fb::create(self::_ID_FIELD, T\IdentifierType::create())
                    ->required()
                    ->withDefault(function() { return UuidIdentifier::generate(); })
                    ->className(UuidIdentifier::class)
                    ->overridable(true)
                    ->build(),
                Fb::create(self::STATUS_FIELD, T\StringEnumType::create())
                    ->withDefault("draft")
                    ->className(NodeStatus::class)
                    ->build(),
                Fb::create(self::ETAG_FIELD, T\StringType::create())
                    ->maxLength(100)
                    ->pattern('^[\w\.:-]+$')
                    ->build(),
                Fb::create(self::CREATED_AT_FIELD, T\MicrotimeType::create())
                    ->build(),
                /*
                 * A fully qualified reference to what created this node. This is intentionally a message-ref
                 * and not a user id because it is often a program that creates nodes, not a user.
                 */
                Fb::create(self::CREATOR_REF_FIELD, T\MessageRefType::create())
                    ->build(),
                Fb::create(self::UPDATED_AT_FIELD, T\MicrotimeType::create())
                    ->useTypeDefault(false)
                    ->build(),
                /*
                 * A fully qualified reference to what updated this node. This is intentionally a message-ref
                 * and not a user id because it is often a program that updates nodes, not a user.
                 * E.g. "acme:iam:node:app:cli-scheduler" or "acme:iam:node:user:60c71df0-fda8-11e5-bfb9-30342d363854"
                 */
                Fb::create(self::UPDATER_REF_FIELD, T\MessageRefType::create())
                    ->build(),
                /*
                 * A reference to the last event that changed the state of this node.
                 * E.g. "acme:blog:event:article-published:60c71df0-fda8-11e5-bfb9-30342d363854"
                 */
                Fb::create(self::LAST_EVENT_REF_FIELD, T\MessageRefType::create())
                    ->build(),
                Fb::create(self::TITLE_FIELD, T\StringType::create())
                    ->build(),
                /*
                 * The roles determine what permissions this app will have when using the system.
                 * The role itself is a node which has a set of "allow" and "deny" rules.
                 */
                Fb::create(self::ROLES_FIELD, T\NodeRefType::create())
                    ->asASet()
                    ->build(),
                /*
                 * An encrypted value of connection-string (starts with Endpoint=sb://) that can be obtained from Azure portal.
                 * @link
                 * https://docs.microsoft.com/en-us/azure/notification-hubs/notification-hubs-nodejs-push-notification-tutorial
                 */
                Fb::create(self::AZURE_NOTIFICATION_HUB_CONNECTION_FIELD, T\TextType::create())
                    ->build(),
                Fb::create(self::AZURE_NOTIFICATION_HUB_NAME_FIELD, T\StringType::create())
                    ->pattern('^[\w\.-]+$')
                    ->build(),
                /*
                 * An encrypted value of legacy-server-key that can be obtained from the cloud messaging tab of the Firebase
                 * console.
                 * @link https://firebase.google.com/docs/cloud-messaging/auth-server
                 */
                Fb::create(self::FCM_API_KEY_FIELD, T\TextType::create())
                    ->build(),
                /*
                 * A unique identifier for your Firebase project, used in requests to the FCM v1 HTTP endpoint.
                 * @link https://firebase.google.com/docs/cloud-messaging/concept-options#projectid
                 */
                Fb::create(self::FCM_PROJECT_ID_FIELD, T\StringType::create())
                    ->format(Format::SLUG())
                    ->build(),
                /*
                 * A unique numerical value created when you create your Firebase project.
                 * @link https://firebase.google.com/docs/cloud-messaging/concept-options#senderid
                 */
                Fb::create(self::FCM_SENDER_ID_FIELD, T\StringType::create())
                    ->pattern('^\d+$')
                    ->build(),
                /*
                 * An un-encrypted key that can be obtained from the cloud messaging tab of the Firebase console.
                 */
                Fb::create(self::FCM_WEB_API_KEY_FIELD, T\StringType::create())
                    ->pattern('^[\w\.-]+$')
                    ->build(),
            ],
            self::MIXINS
        );
    }
}
